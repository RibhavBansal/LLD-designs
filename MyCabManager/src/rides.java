public class rides {
    public int startX;
    public int startY;
    public int destX;
    public int destY;
    public Driver driver;

    rides(int startX, int startY, int destX, int destY, Driver driver) {
        this.startX = startX;
        this.startY = startY;
        this.destX = destX;
        this.destY = destY;
        this.driver = driver;
    }
}
